//MulDemo.java
class Mul 
{
	int a,b,c;
	void accept()
	{
		a=10;
		b=20;
	}
	void multiply()
	{
		c=a*b;
	}
	void disp()
	{
		System.out.println("value of a="+a);
		System.out.println("value of b="+b);
		System.out.println("value of c="+c);
	}
}//Mul--BLC
class MulDemo
{
	public static void main(String[] args) 
	{
		Mul m=new Mul();
		m.accept();
		m.multiply();
		m.disp();

	}
}
